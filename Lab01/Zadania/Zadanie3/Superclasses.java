public class Superclasses {
    public static void main(String[] args) {
        System.out.println("Superclass of String: " + String.class.getSuperclass());
        System.out.println("Superclass of Scanner: " + java.util.Scanner.class.getSuperclass());
        System.out.println("Superclass of ImageOutputStream: " + javax.imageio.stream.ImageOutputStream.class.getSuperclass());
    }
}
